#!/usr/bin/env node
'use strict';

/*
 * build-canonical-manifest.js
 *
 * Purpose
 * -------
 * Walk the framework tree declared by the `files` whitelist in package.json
 * and emit the CANONICAL manifest at .kaizen-dvir/manifest.json.
 *
 * This canonical manifest is shipped INSIDE the published npm tarball. Once
 * installed under node_modules/@DanYuzo/kaizen-dvir/.kaizen-dvir/manifest.json
 * it becomes the source of truth that `kaizen update` (M6.2) reads to detect
 * drift against the expert's local manifest.
 *
 * It is DISTINCT from the local manifest written by `kaizen init` and `kaizen
 * update` into the expert's own project root. The local manifest reflects the
 * expert's installed state; this canonical manifest reflects what shipped in
 * the published version.
 *
 * Constraints
 * -----------
 * - Stdlib only (CON-003) — uses fs, path, crypto, os.
 * - Idempotent — re-running on unchanged source produces an identical
 *   manifest body. The `generatedAt` timestamp lives at the file root, not
 *   inside per-file entries, so file entries are byte-stable.
 * - The manifest must be regenerated by CI BEFORE `npm pack`, and may be
 *   regenerated locally for development.
 *
 * Schema (per Q1 — 2026-04-25)
 * ----------------------------
 * {
 *   "version": "1.5.0-rc.0",            // mirrors package.json version
 *   "generatedAt": "ISO-8601 timestamp",
 *   "generator": "build-canonical-manifest@1",
 *   "files": {
 *     "<relative-path>": {
 *       "hash":  "sha256:<hex>",
 *       "layer": "L1" | "L2" | "L3" | "L4-readonly",
 *       "size":  <bytes>                // Q1: additive — used for diagnostics
 *     }
 *   }
 * }
 *
 * Layer assignment
 * ----------------
 * Layer is derived from path prefix (consistent with v1.4 framework boundary
 * declared in dvir-config.yaml):
 *
 *   bin/                                     -> L1
 *   .kaizen-dvir/dvir/                       -> L1
 *   .kaizen-dvir/commandments.md             -> L1
 *   .kaizen-dvir/dvir-config.yaml            -> L1
 *   .kaizen-dvir/instructions/               -> L2
 *   .kaizen-dvir/celulas/                    -> L2  (with MEMORY.md exception applied at update time, not here)
 *   .kaizen-dvir/infra/                      -> L2
 *   .kaizen-dvir/refs/                       -> L2
 *
 * The canonical manifest does NOT enumerate L3 / L4 paths because L3 (.claude/)
 * and L4 (expert-owned celulas/, refs/, .kaizen/) do not exist in the package —
 * they are scaffolded into the expert's project at init time.
 *
 * Exit codes
 * ----------
 * 0 — manifest written successfully.
 * 1 — fatal error (missing whitelist source, IO failure, etc.).
 *
 * Usage
 * -----
 *   node bin/build-canonical-manifest.js
 *   npm run build:manifest
 */

const fs = require('node:fs');
const path = require('node:path');
const crypto = require('node:crypto');

const REPO_ROOT = path.resolve(__dirname, '..');
const PACKAGE_JSON = path.join(REPO_ROOT, 'package.json');
const MANIFEST_OUT = path.join(REPO_ROOT, '.kaizen-dvir', 'manifest.json');

const LAYER_RULES = [
  // L1 — framework core (overwrite always on update)
  { prefix: 'bin/', layer: 'L1' },
  { prefix: '.kaizen-dvir/dvir/', layer: 'L1' },
  { exact: '.kaizen-dvir/commandments.md', layer: 'L1' },
  { exact: '.kaizen-dvir/dvir-config.yaml', layer: 'L1' },
  // L2 — framework templates / instructions (overwrite with MEMORY.md exception)
  { prefix: '.kaizen-dvir/instructions/', layer: 'L2' },
  { prefix: '.kaizen-dvir/celulas/', layer: 'L2' },
  { prefix: '.kaizen-dvir/infra/', layer: 'L2' },
  { prefix: '.kaizen-dvir/refs/', layer: 'L2' },
];

// Files that MUST never appear in the canonical manifest because they live
// outside the publish whitelist. Defense in depth — the whitelist is the
// primary gate; this list catches mistakes if a builder is invoked in a tree
// where these paths are reachable but should not be hashed.
const EXCLUDE_DIRS = new Set([
  'tests',
  'docs',
  '.github',
  '.kaizen',
  'node_modules',
  '.git',
]);

// Files inside whitelisted paths that are noise and should not be hashed.
// `.gitkeep` is a structural marker and must remain in the tarball but is not
// part of the framework's executable surface — including it would make the
// manifest noisier without value. Keep it OUT of the manifest but IN the
// tarball (the publish whitelist still picks it up).
const SKIP_FILE_NAMES = new Set(['.gitkeep']);

function readPackageJson() {
  const raw = fs.readFileSync(PACKAGE_JSON, 'utf8');
  return JSON.parse(raw);
}

function classifyLayer(relPath) {
  // Normalize to forward slashes so prefix checks work cross-platform.
  const norm = relPath.split(path.sep).join('/');
  for (const rule of LAYER_RULES) {
    if (rule.exact && norm === rule.exact) return rule.layer;
    if (rule.prefix && norm.startsWith(rule.prefix)) return rule.layer;
  }
  return null;
}

function isExcluded(relPath) {
  const head = relPath.split(path.sep)[0];
  if (EXCLUDE_DIRS.has(head)) return true;
  return false;
}

function hashFile(absPath) {
  const buf = fs.readFileSync(absPath);
  const hex = crypto.createHash('sha256').update(buf).digest('hex');
  return { hash: 'sha256:' + hex, size: buf.length };
}

function walk(absPath, relPath, out) {
  const stat = fs.statSync(absPath);
  if (stat.isDirectory()) {
    if (relPath && isExcluded(relPath)) return;
    const entries = fs.readdirSync(absPath).sort();
    for (const entry of entries) {
      const childAbs = path.join(absPath, entry);
      const childRel = relPath ? path.join(relPath, entry) : entry;
      walk(childAbs, childRel, out);
    }
    return;
  }
  if (!stat.isFile()) return;
  const baseName = path.basename(absPath);
  if (SKIP_FILE_NAMES.has(baseName)) return;
  if (isExcluded(relPath)) return;
  const layer = classifyLayer(relPath);
  if (!layer) return; // outside framework whitelist — skip
  const { hash, size } = hashFile(absPath);
  // Always store with forward slashes for cross-platform stable manifest
  const key = relPath.split(path.sep).join('/');
  out[key] = { hash, layer, size };
}

function whitelistRoots(pkg) {
  // The package.json `files` array drives what gets walked. Each entry can be
  // a directory (with or without trailing slash) or a single file.
  const files = Array.isArray(pkg.files) ? pkg.files : [];
  return files.map((f) => f.replace(/\/+$/, ''));
}

function main() {
  let pkg;
  try {
    pkg = readPackageJson();
  } catch (err) {
    process.stderr.write('FATAL: cannot read package.json: ' + err.message + '\n');
    return 1;
  }

  const roots = whitelistRoots(pkg);
  if (roots.length === 0) {
    process.stderr.write('FATAL: package.json `files` whitelist is empty\n');
    return 1;
  }

  const filesOut = {};
  for (const rel of roots) {
    const abs = path.join(REPO_ROOT, rel);
    if (!fs.existsSync(abs)) {
      // Non-fatal: a whitelist may legitimately reference a target that does
      // not yet exist (e.g., the manifest itself before first generation).
      // Skip silently when the missing entry IS the manifest output, fatal
      // otherwise so drift is visible.
      const norm = rel.split(path.sep).join('/');
      if (norm === '.kaizen-dvir/manifest.json') continue;
      process.stderr.write('WARN: whitelist entry missing on disk: ' + rel + '\n');
      continue;
    }
    walk(abs, rel, filesOut);
  }

  const manifest = {
    version: pkg.version,
    generatedAt: new Date().toISOString(),
    generator: 'build-canonical-manifest@1',
    files: sortByKey(filesOut),
  };

  // Ensure target directory exists
  fs.mkdirSync(path.dirname(MANIFEST_OUT), { recursive: true });
  fs.writeFileSync(MANIFEST_OUT, JSON.stringify(manifest, null, 2) + '\n', 'utf8');

  const fileCount = Object.keys(manifest.files).length;
  process.stdout.write(
    'canonical manifest written: ' +
      path.relative(REPO_ROOT, MANIFEST_OUT) +
      ' (' + fileCount + ' files, version ' + manifest.version + ')\n'
  );
  return 0;
}

function sortByKey(obj) {
  const keys = Object.keys(obj).sort();
  const sorted = {};
  for (const k of keys) sorted[k] = obj[k];
  return sorted;
}

if (require.main === module) {
  process.exit(main());
}

module.exports = {
  // Exposed for tests in tests/m6/
  classifyLayer,
  hashFile,
  whitelistRoots,
  walk,
  REPO_ROOT,
  MANIFEST_OUT,
};
